#include "shell.h"

/**
 * input_buf - Read input from the user into a buffer.
 * @info: A pointer to the info_t structure containing shell information.
 * @buf: A pointer to the buffer that will store the input.
 * @len: A pointer to the size of the buffer.
 *
 * This function reads input from the user and stores it in the provided buffer
 * It ensures that the buffer is dynamically resized as needed to accommodate
 * the input. The length of the input is stored in 'len'.
 *
 * Return: The number of bytes read (including newline character), or -1 error.
 */
ssize_t input_buf(info_t *info, char **buf, size_t *len)
{
	ssize_t r = 0;
	size_t len_p = 0;

	if (!*len) /* if nothing left in the buffer, fill it */
	{
		/*bfree((void **)info->cmd_buf);*/
		free(*buf);
		*buf = NULL;
		signal(SIGINT, sigintHandler);
#if USE_GETLINE
		r = getline(buf, &len_p, stdin);
#else
		r = _getline(info, buf, &len_p);
#endif
		if (r > 0)
		{
			if ((*buf)[r - 1] == '\n')
			{
				(*buf)[r - 1] = '\0'; /* remove trailing newline */
				r--;
			}
			info->linecount_flag = 1;
			remove_comments(*buf);
			build_history_list(info, *buf, info->histcount++);
			/* if (_strchr(*buf, ';')) is this a command chain? */
			{
				*len = r;
				info->cmd_buf = buf;
			}
		}
	}
	return (r);
}

/**
 * get_input - Read input from the user and handle history.
 * @info: A pointer to the info_t structure containing shell information.
 *
 * This function reads input from the user, handles history, and ensures the
 * input is properly formatted and stored in 'info->arg'. It provides command
 * history functionality by navigating through previous shell commands.
 *
 * Return: The number of bytes read, 0 if input was empty, or -1 on error.
 */
ssize_t get_input(info_t *info)
{
	static char *buf; /* the ';' command chain buffer */
	static size_t i, j, len;
	ssize_t r = 0;
	char **buf_p = &(info->arg), *p;

	_putchar(BUF_FLUSH);
	r = input_buf(info, &buf, &len);
	if (r == -1) /* EOF */
		return (-1);
	if (len)	/* we have commands left in the chain buffer */
	{
		j = i; /* init new iterator to current buf position */
		p = buf + i; /* get pointer for return */

		check_chain(info, buf, &j, i, len);
		while (j < len) /* iterate to semicolon or end */
		{
			if (is_chain(info, buf, &j))
				break;
			j++;
		}

		i = j + 1; /* increment past nulled ';'' */
		if (i >= len) /* reached end of buffer? */
		{
			i = len = 0; /* reset position and length */
			info->cmd_buf_type = CMD_NORM;
		}

		*buf_p = p; /* pass back pointer to current command position */
		return (_strlen(p)); /* return length of current command */
	}

	*buf_p = buf; /* else not a chain, pass back buffer from _getline() */
	return (r); /* return length of buffer from _getline() */
}

/**
 * read_buf - Read characters from input buffer and handle backspacing.
 * @info: A pointer to the info_t structure containing shell information.
 * @buf: The buffer to read characters into.
 * @i: The current index in the buffer.
 *
 * This function reads characters from the input buffer, handling backspaces
 * and erasing characters if needed. It ensures that the input is properly
 * formatted and updates the buffer accordingly.
 *
 * Return: The number of characters read into the buffer.
 */
ssize_t read_buf(info_t *info, char *buf, size_t *i)
{
	ssize_t r = 0;

	if (*i)
		return (0);
	r = read(info->readfd, buf, READ_BUF_SIZE);
	if (r >= 0)
		*i = r;
	return (r);
}

/**
 * _getline - Read a line from input and allocate memory for it.
 * @info: A pointer to the info_t structure containing shell information.
 * @ptr: A pointer to a buffer where the line will be stored.
 * @length: A pointer to the size of the allocated buffer.
 *
 * This function reads a line from the input and dynamically allocates memory
 * to store it. It handles reallocation as needed and ensures proper error
 * checking.
 *
 * Return: The number of characters read, or -1 on failure.
 */
int _getline(info_t *info, char **ptr, size_t *length)
{
	static char buf[READ_BUF_SIZE];
	static size_t i, len;
	size_t k;
	ssize_t r = 0, s = 0;
	char *p = NULL, *new_p = NULL, *c;

	p = *ptr;
	if (p && length)
		s = *length;
	if (i == len)
		i = len = 0;

	r = read_buf(info, buf, &len);
	if (r == -1 || (r == 0 && len == 0))
		return (-1);

	c = _strchr(buf + i, '\n');
	k = c ? 1 + (unsigned int)(c - buf) : len;
	new_p = _realloc(p, s, s ? s + k : k + 1);
	if (!new_p) /* MALLOC FAILURE! */
		return (p ? free(p), -1 : -1);

	if (s)
		_strncat(new_p, buf + i, k - i);
	else
		_strncpy(new_p, buf + i, k - i + 1);

	s += k - i;
	i = k;
	p = new_p;

	if (length)
		*length = s;
	*ptr = p;
	return (s);
}

/**
 * sigintHandler - Handle the SIGINT signal (Ctrl+C) in the shell.
 * @sig_num: The signal number (unused).
 *
 * Return: void
 */
void sigintHandler(__attribute__((unused))int sig_num)
{
	_puts("\n");
	_puts("$ ");
	_putchar(BUF_FLUSH);
}
